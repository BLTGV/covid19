export const country3toName = (code3: string) => {
  const code2 = country3to2[code3];
  const name = country2toName[code2];
  return name;
};

export const country2toFlagPath = (code2: string) => {
  const path = `/flags/${code2.toLowerCase()}.svg`;
  return path;
};

export const country3toFlagPath = (code3: string) => {
  let c3 = code3;
  if (code3 === "United States") c3 = "USA";
  const code2 = country3to2[c3];
  const path = country2toFlagPath(code2);
  return path;
};

export const countryToFlagPath = (location: LocationData) => {
  const { countryId, name, country } = location;
  if (isNil(countryId)) {
    const countryName = country ? country : name;
    if (isNil(countryName) || isEmpty(countryName))
      return country2toFlagPath("unknown");
    const countryLength = countryName.length;
    if (countryLength === 2) return country2toFlagPath(countryName);
    if (countryLength === 3) return country3toFlagPath(countryName);
    return country2toFlagPath("unknown");
  }
  const [code2] = reverse(countryId.split(":"));
  return country2toFlagPath(code2);
};
